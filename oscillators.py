class Oscillator(object):
    GRID_SIZE = (0, 0)
    PERIOD = 0
    PATTERN = [[]]

    def __init__(self, grid_size=None, pattern=None, period=None):
        self.behavior = 'OSCILLATE'
        self.grid_size = grid_size if grid_size else self.GRID_SIZE
        self.pattern = pattern if pattern else self.PATTERN
        self.period = period if period else self.PERIOD


